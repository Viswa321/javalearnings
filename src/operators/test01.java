public class test01 {


    public static void main(String[] args) {
        //increment operator
        int a = 10;
        //post --->a++ // after a ++
        //pre ---->++a // before a ++
         System.out.println(a++);// after this statement value of a = 11
        System.out.println(a);

        System.out.println(++a +  a++ + ++a);
        //a++ --> Exp value = 11 , a = 11
        //++a __> Exp value = 11 , a = 12
        // a++ --> Exp value = 12 ,a = 13
        System.out.println(a);





    }


}
